package GasStation;

public class Discount {
		private int amount;
	 	private int code;
	 	private String name;
	 	private Progres status;
	 	
	 		public int getAmount() {
				return amount;
			}
	 	
	 		public int getCode() {
	 			return code;
	 		}
	 		
	 		public String getName() {
	 			return name;
	 		}
	 		
	 		public Progres getStatus() {
	 			return status;
	 		}
	 		
	 		public void setAmount(int amount) {
				this.amount = amount;
			}
	 		
	 		public void setCode(int code) {
	 			this.code = code;
	 		}
	 		
	 		public void setName(String name) {
	 			this.name = name;
	 		}
	 		
	 		public void setStatus(Progres status) {
	 			this.status = status;
	 		}
	 
	 
}
